#Função: Funções utilitárias
def soma(a, b):
    return a + b

def subtracao(a, b):
    return a - b

def multiplicacao(a, b):
    return a * b

def divisao(a, b):
    return a / b

def potencia(a, b):
    return a ** b

def raiz_quadrada(a):
    return a ** 0.5

def raiz_cubica(a):
    return a ** (1/3)

def logaritmo(a, b):
    return a ** (1/b)

def logaritmo_natural(a):
    return logaritmo(a, 10)